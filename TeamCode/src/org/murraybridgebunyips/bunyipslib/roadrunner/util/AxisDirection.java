package org.murraybridgebunyips.bunyipslib.roadrunner.util;

/**
 * A direction for an axis to be remapped to
 */
public enum AxisDirection {
    /**
     * Forward
     */
    POS_X,
    /**
     * Backward
     */
    NEG_X,
    /**
     * Right
     */
    POS_Y,
    /**
     * Left
     */
    NEG_Y,
    /**
     * Up (anticlockwise)
     */
    POS_Z,
    /**
     * Down (clockwise)
     */
    NEG_Z
}
